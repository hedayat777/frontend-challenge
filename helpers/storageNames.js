function StorageName(name) {
    if (name === 'name') {
        return 'frontend-challenge-name'
    }else if(name === 'email'){
        return 'frontend-challenge-email'
    }else if(name === 'newsletter'){
        return 'frontend-challenge-newsletter'
    }else if(name === 'age'){
        return 'frontend-challenge-age'
    }else if(name === 'startStep1'){
        return 'frontend-challenge-startStep1'
    }else if(name === 'startStep2'){
        return 'frontend-challenge-startStep2'
    }else if(name === 'lastStep'){
        return 'frontend-challenge-lastStep'
    }
}
